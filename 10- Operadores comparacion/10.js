/* 
Operadores de comparacion

Sirven para comparar cantidades o valores entre variables.

Asignacion =
Mayor que >
Menor que <
Igual que ==
Diferente de !=
Menor o igual a <=
Mayor o igual a >=



*/

let valor1, valor2; //Se declaran las variables

valor1 = 20; //Inicializar las variables
valor2 = 10;

//Operador Mayor que (>)
document.writeln(valor1 > valor2);
document.writeln("<br>"); // El sistema verifica si el resultado es verdadero o falso

//Operador menor que (<)
document.writeln(valor1 < valor2);
document.writeln("<br>");

//Operador igual que (=)
document.writeln(valor1 == valor2);
document.writeln("<br>");

//Operador diferente que (!=)
document.writeln(valor1 != valor2);
document.writeln("<br>");

//Operador menor o igual que (<=)
document.writeln(valor1 <= valor2);
document.writeln("<br>");

//Operador Mayor o igual que (>=)
document.writeln(valor1 >= valor2);
document.writeln("<br>");